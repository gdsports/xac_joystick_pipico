/*
 * Modification of dev_hid_composite example to create a simple joystick.
 * 2 axes and 8 buttons. Compatible with the Xbox Adaptive Controller.
 */

#include <stdlib.h>
#include <stdio.h>
#include <string.h>

#include "bsp/board.h"
#include "tusb.h"
#include "usb_descriptors.h"

//--------------------------------------------------------------------+
// MACRO CONSTANT TYPEDEF PROTYPES
//--------------------------------------------------------------------+

/* Blink pattern
 * - 250 ms  : device not mounted
 * - 1000 ms : device mounted
 * - 2500 ms : device is suspended
 */
enum {
    BLINK_NOT_MOUNTED = 250,
    BLINK_MOUNTED = 1000,
    BLINK_SUSPENDED = 2500,
};

static uint32_t blink_interval_ms = BLINK_NOT_MOUNTED;

void led_blinking_task(void);
void hid_task(void);

/*------------- MAIN -------------*/
int main(void) {
    board_init();
    tusb_init();

    while (1) {
        tud_task(); // tinyusb device task
        led_blinking_task();
        hid_task();
    }

    return 0;
}

//--------------------------------------------------------------------+
// Device callbacks
//--------------------------------------------------------------------+

// Invoked when device is mounted
void tud_mount_cb(void) {
    blink_interval_ms = BLINK_MOUNTED;
}

// Invoked when device is unmounted
void tud_umount_cb(void) {
    blink_interval_ms = BLINK_NOT_MOUNTED;
}

// Invoked when usb bus is suspended
// remote_wakeup_en : if host allow us  to perform remote wakeup
// Within 7ms, device must draw an average of current less than 2.5 mA from bus
void tud_suspend_cb(bool remote_wakeup_en) {
    (void) remote_wakeup_en;
    blink_interval_ms = BLINK_SUSPENDED;
}

// Invoked when usb bus is resumed
void tud_resume_cb(void) {
    blink_interval_ms = BLINK_MOUNTED;
}

//--------------------------------------------------------------------+
// USB HID
//--------------------------------------------------------------------+

void hid_task(void) {
    // Poll every 10ms
    const uint32_t interval_ms = 100;
    static uint32_t start_ms = 0;
    static uint8_t button = 0;
    static HID_JoystickReport_Data_t report = {1, 0, 0};

    if ((board_millis() - start_ms) < interval_ms) return; // not enough time
    start_ms = board_millis() + interval_ms;

    // Remote wakeup
    if (tud_suspended()) {
        // Wake up host if we are in suspend mode
        // and REMOTE_WAKEUP feature is enabled by host
        tud_remote_wakeup();
    }

    /*------------- Joystick -------------*/
    if (tud_hid_ready()) {
        report.xAxis = 128;           // 0=left, 128=center, 255=right
        report.yAxis = 128;           // 0=up, 128=center, 255=down
        report.buttons = 1 << button; // set bit to press a button
        button = (button + 1) & 0x07; // limit button to the range 0..7

        tud_hid_report(0x00, &report, sizeof(report));
    }
}


